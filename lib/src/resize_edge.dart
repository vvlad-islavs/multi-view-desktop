/// Edge or corner used when starting a native resize drag (`DragToResizeArea`).
enum ResizeEdge {
  /// Top edge.
  top,

  /// Left edge.
  left,

  /// Right edge.
  right,

  /// Bottom edge.
  bottom,

  /// Top-left corner.
  topLeft,

  /// Bottom-left corner.
  bottomLeft,

  /// Top-right corner.
  topRight,

  /// Bottom-right corner.
  bottomRight,
}
